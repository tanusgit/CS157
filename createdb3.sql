CREATE TABLE YELP_USER (
YELP_ID VARCHAR(20) PRIMARY KEY,
EMAIL VARCHAR(50),
FIRST_NAME VARCHAR(40),
LAST_NAME VARCHAR(40),
DOB VARCHAR(20),
BIRTH_PLACE VARCHAR(40),
GENDER VARCHAR(20),
FRIENDLIST VARCHAR(50),
COMPLIMENTED_FRIENDLIST VARCHAR (50),
CHECKEDIN_BUSINESS VARCHAR(60)
);

CREATE TABLE BUSINESS_CATEGORY(
    BUSINESS_CATEGORY_ID VARCHAR(250) PRIMARY KEY,
    BUSINESS_CATEGORY_NAME VARCHAR(250),
    LIST_OF_SUBCATEGORIES VARCHAR (250)
);
CREATE TABLE BUSINESS(
    BUSINESS_ID VARCHAR(250) PRIMARY KEY,
    BUSINESS_NAME VARCHAR(250),
    DAYS_OF_OPERATION VARCHAR(40),
    AMBIENT_TYPE VARCHAR(40),
    Address VARCHAR(250),
    BUSINESSCATEGORY VARCHAR(250),
    FOREIGN KEY (BUSINESSCATEGORY) REFERENCES BUSINESS_CATEGORY(BUSINESS_CATEGORY_ID) 
);
CREATE TABLE REVIEWS(
    REVIEW_ID VARCHAR(20) PRIMARY KEY,
    STARS INTEGER,
    AUTHOR VARCHAR(20),
    PUBLISH_DATE TIMESTAMP WITH TIME ZONE,
    BUSINESSID VARCHAR(250),
    FOREIGN KEY (BUSINESSID) REFERENCES BUSINESS(BUSINESS_ID),
    LIST_OF_COMMENTS VARCHAR(60),
    FOREIGN KEY (AUTHOR) REFERENCES YELP_USER(YELP_ID)
);

CREATE TABLE CHECKIN(
    ID INTEGER PRIMARY KEY,
    INFO VARCHAR(250),
    BUSN_ID VARCHAR(250),
    FOREIGN KEY (BUSN_ID) REFERENCES BUSINESS(BUSINESS_ID)
);


